public class BuscarAnxF {
    
    String folio="",numero="",fecha="";

    public BuscarAnxF(String folio, String numero, String fecha) {
        this.folio = folio;
        this.numero = numero;
        this.fecha = fecha;
    }

    public String getFolio() {
        return folio;
    }

    public void setFolio(String folio) {
        this.folio = folio;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }
    
    
}
